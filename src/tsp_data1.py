def compute_distance_matrix(coords):
    """Compute Euclidean distance matrix with TSPLIB nint rounding."""
    n = len(coords)
    coords_arr = np.array(coords, dtype=np.float64)
    dist = np.zeros((n, n), dtype=np.float64)
    for i in range(n):
        for j in range(i + 1, n):
            dx = coords_arr[i, 0] - coords_arr[j, 0]
            dy = coords_arr[i, 1] - coords_arr[j, 1]
            d = np.round(np.sqrt(dx * dx + dy * dy))
            dist[i, j] = d
            dist[j, i] = d
    return dist


def tour_length(tour, dist_matrix):
    """Calculate total tour length for a given tour (permutation)."""
    n = len(tour)
    length = 0.0
    for i in range(n):
        length += dist_matrix[tour[i], tour[(i + 1) % n]]
    return length


def nearest_neighbor_tour(dist_matrix, start=0):
    """Generate initial tour using nearest-neighbor heuristic."""
    n = dist_matrix.shape[0]
    visited = [False] * n
    tour = [start]
    visited[start] = True
    for _ in range(n - 1):
        current = tour[-1]
        best_next = -1
        best_dist = float('inf')
        for j in range(n):
            if not visited[j] and dist_matrix[current, j] < best_dist:
                best_dist = dist_matrix[current, j]
                best_next = j
        tour.append(best_next)
        visited[best_next] = True
    return tour


def get_instance(name):
    """Return (coords, dist_matrix, optimal) for a named instance."""
    instances = {
        'eil51': EIL51_COORDS,
        'berlin52': BERLIN52_COORDS,
        'st70': ST70_COORDS,
        'kroA100': KROA100_COORDS,
    }
    coords = instances[name]
    dist_matrix = compute_distance_matrix(coords)
    optimal = OPTIMAL[name]
    return coords, dist_matrix, optimal
